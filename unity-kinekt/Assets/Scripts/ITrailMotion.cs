using UnityEngine;

// Геометрия одной копии трейла для данного возраста.
// age: 0 = только что записана, 1 = самая старая в буфере.
public struct TrailTransform
{
    public Vector2 uvOffset;   // смещение в UV-координатах (0..1 — размер кадра)
    public Vector2 uvScale;    // масштаб от центра кадра; (1,1) = без изменений
    public float depthAlpha;   // 0..1 — дополнительное затухание (имитация "дальше/ближе")
    public bool mirror;        // отразить по X (для "обратной стороны" при вращении)
}

// Любой скрипт, реализующий этот интерфейс и добавленный на тот же объект,
// что и OutlineCompositor, подменяет собой стандартный линейный дрифт трейла.
public interface ITrailMotion
{
    TrailTransform GetTransform(float age);

    // Используется для сортировки порядка отрисовки (дальше -> ближе).
    // Больше значение = ближе к зрителю = рисуется позже (поверх остальных).
    float SortDepth(float age);
}
